import React, { createContext, useContext, useState, useCallback, useEffect } from 'react';
import { AppState, AppStateStatus } from 'react-native';
import { StorageEventBus, STORAGE_KEYS } from '@/lib/storage-event-bus';
import AsyncStorage from '@react-native-async-storage/async-storage';
import {
  sendSubscriptionApprovedNotification,
  sendSubscriptionRejectedNotification,
  sendSubscriptionActivatedNotification,
} from './subscription-notification-service';
import { sendNotification } from './send-notification';

const STORAGE_KEY_REQUESTS = STORAGE_KEYS.SUBSCRIPTION_REQUESTS;
const STORAGE_KEY_HISTORY = STORAGE_KEYS.SUBSCRIPTION_HISTORY;

export interface SubscriptionRequest {
  id: string;
  userId: string;
  userName: string;
  userPhone: string;
  userRole: 'residential' | 'commercial' | 'industrial' | 'collector' | 'recycler';
  subscriptionPlan: string;
  planId?: string;
  planPrice: number;
  affiliationFee?: number;
  requestDate: string;
  status: 'pending' | 'approved' | 'rejected' | 'activated';
  rejectionReason?: string;
  approvedBy?: string;
  approvalDate?: string;
  activationDate?: string;
  paymentMethod?: string;
  /** The payment reference generated by createPayment (e.g. LTC1A2B3C) */
  paymentReference?: string;
  /** The internal payment record ID from PaymentsContext */
  paymentId?: string;
  /** The transaction ID entered by the user (from their mobile money SMS) */
  transactionId?: string;
  /** Amount paid in ZMW */
  amountPaid?: number;
  notes?: string;
}

export interface ApprovalAction {
  id: string;
  requestId: string;
  adminId: string;
  adminName: string;
  adminRole: 'superadmin' | 'finance';
  action: 'approved' | 'rejected' | 'activated';
  reason?: string;
  timestamp: string;
  notes?: string;
}

/**
 * Callback used by activateAccount to persist the subscription to the user's
 * auth profile. Injected via SubscriptionApprovalProvider props so the context
 * does not need to import auth-context (avoids circular deps).
 */
export type ActivateSubscriptionCallback = (
  userId: string,
  subscription: {
    planId: string;
    planName: string;
    expiresAt: string;
    pickupsRemaining: number;
  },
) => Promise<void>;

interface SubscriptionApprovalContextType {
  pendingRequests: SubscriptionRequest[];
  approvalHistory: ApprovalAction[];
  isLoading: boolean;
  approveRequest: (requestId: string, adminId: string, adminName: string, adminRole: string, notes?: string) => void;
  rejectRequest: (requestId: string, adminId: string, adminName: string, reason: string, notes?: string) => void;
  activateAccount: (requestId: string, adminId: string, adminName: string) => void;
  getRequestById: (requestId: string) => SubscriptionRequest | undefined;
  getRequestsByStatus: (status: SubscriptionRequest['status']) => SubscriptionRequest[];
  /** Returns all requests for a specific userId, sorted newest first */
  getRequestsByUserId: (userId: string) => SubscriptionRequest[];
  getApprovalHistory: (requestId?: string) => ApprovalAction[];
  addSubscriptionRequest: (request: Omit<SubscriptionRequest, 'id' | 'status' | 'requestDate'>) => Promise<string>;
  getStats: () => { pending: number; approved: number; activated: number; rejected: number };
}

const SubscriptionApprovalContext = createContext<SubscriptionApprovalContextType | undefined>(undefined);

